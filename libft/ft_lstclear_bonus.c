#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*ptr;

	if (!lst || !del)
		return ;
	ptr = *lst;
	while (*lst)
	{
		ptr = ptr ->next;
		ft_lstdelone(*lst, del);
		*lst = ptr;
	}
}
